    def btn_randomisieren_clicked(self):
        has_geometry = bool(self.maptool.all_points) or is_layer_valid(self.plot_layer)
        if not has_geometry:
            QMessageBox.warning(self, self.tr("Fehler"), self.tr("Bitte erstellen Sie zuerst Parzellen, bevor Sie randomisieren."))
            return False

        if self.maptool.all_points:
            # Normal path: build plots from maptool geometry and clip to boundary.
            if is_layer_valid(self.plot_layer):
                remove_layer(self.plot_layer)
                self.plot_layer = None

            plot_layer = self._create_plot_layer()
            feats = self._build_randomized_features()
            plot_layer.dataProvider().addFeatures(feats)
            plot_layer.commitChanges()
            self.plot_layer = self._intersect_plots_with_boundary(plot_layer)
        else:
            # Restore path: all_points lost (e.g. after project reload) but the
            # already-clipped plot_layer is present — re-shuffle variant assignments.
            self.plot_layer = self._rerandomize_existing_plot_layer()

        apply_categorized_variant_renderer(self.plot_layer, list(self.faktorstufen1))
        self._add_plot_layer_to_project()
        self.clear_maptool()

        # Keep core plots in sync with the new trial plot layout.
        if self.core_plot_layers and is_layer_valid(self.plot_layer):
            self._do_create_core_plots(self.plot_layer)

        return True

    def _rerandomize_existing_plot_layer(self):
        """Re-shuffle variant assignments on an already-clipped plot layer.

        Used when maptool.all_points is not available (e.g. after project reload)
        but self.plot_layer exists with saved geometries.
        """
        geometries = [f.geometry() for f in self.plot_layer.getFeatures()]
        crs_auth = self.plot_layer.crs().authid()
        remove_layer(self.plot_layer)
        self.plot_layer = None

        new_layer = QgsVectorLayer(
            f"Polygon?crs={crs_auth}", self.tr("Versuchsparzellen"), "memory")
        new_layer.dataProvider().addAttributes([
            QgsField("ID", QMetaType.Type.Int, "int"),
            QgsField("LABEL", QMetaType.Type.QString, "string"),
            QgsField("WDH", QMetaType.Type.Int, "int"),
            QgsField("FAKTOR1", QMetaType.Type.QString, "string"),
            QgsField("FAKTOR2", QMetaType.Type.QString, "string"),
            QgsField("VARIANTE", QMetaType.Type.QString, "string"),
        ])
        new_layer.updateFields()
        new_layer.startEditing()

        no_variants = len(self.faktorstufen1)
        array_variants = list(self.faktorstufen1)
        random.shuffle(array_variants)
        block_no = 1
        feats = []

        for fid, geom in enumerate(geometries, start=1):
            feature = QgsFeature(fid)
            feature.setGeometry(geom)
            variant = array_variants.pop(0)
            feature.setAttributes([
                fid, f"{block_no}_{variant}", block_no,
                self.faktor1, self.faktor2, variant,
            ])
            feats.append(feature)
            if fid % no_variants == 0:
                block_no += 1
            if not array_variants:
                array_variants = list(self.faktorstufen1)
                random.shuffle(array_variants)

        new_layer.dataProvider().addFeatures(feats)
        new_layer.commitChanges()
        return new_layer

