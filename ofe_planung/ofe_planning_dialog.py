    def _build_randomized_features(self):
        """Build randomized plot features from maptool geometry."""
        feats = []
        no_variants = len(self.faktorstufen1)
        array_variants = list(self.faktorstufen1)
        random.shuffle(array_variants)
        block_no = 1
        fid = 1

        for p in self.maptool.all_points:
            poly = QgsPolygon()
            linestring = QgsLineString(p)
            linestring.close()
            poly.setExteriorRing(linestring)
            feature = QgsFeature(fid)
            feature.setGeometry(poly)

            variant = array_variants.pop(0)
            feature.setAttributes([
                fid, f"{block_no}_{variant}", block_no,
                f"{self.faktor1}", self.faktor2, variant,
            ])
            feats.append(feature)

            if fid % no_variants == 0:
                block_no += 1
            fid += 1

            if not array_variants:
                array_variants = list(self.faktorstufen1)
                random.shuffle(array_variants)

        return feats

    def _intersect_plots_with_boundary(self, plot_layer):
        """Clip plots to the inner boundary (or field boundary as fallback), then subtract exclusion areas."""
        clip_layer = self.innerLayer
        if clip_layer is None or not clip_layer.isValid():
            clip_layer = self.feld_layer
        if clip_layer is None or not clip_layer.isValid():
            logger.warning("Inner boundary and field boundary not set. No clipping possible.")
            return plot_layer

        context = QgsProcessingContext()
        context.setProject(QgsProject.instance())
        plots_extracted = processing.run("native:intersection", {
            'INPUT': plot_layer,
            'OVERLAY': clip_layer,
            'INPUT_FIELDS': [], 'OVERLAY_FIELDS': [],
            'OVERLAY_FIELDS_PREFIX': '',
            'OUTPUT': 'TEMPORARY_OUTPUT',
            'GRID_SIZE': None,
        }, context=context, feedback=QgsProcessingFeedback())["OUTPUT"]

        # Subtract exclusion areas (obstacles) if present
        if is_layer_valid(self.af_layer) and self.af_layer.featureCount() > 0:
            plots_extracted = processing.run("native:difference", {
                'INPUT': plots_extracted,
                'OVERLAY': self.af_layer,
                'OUTPUT': 'TEMPORARY_OUTPUT',
                'GRID_SIZE': None,
            }, context=context, feedback=QgsProcessingFeedback())["OUTPUT"]

        plots_extracted.setName(self.tr("Versuchsparzellen"))
        return plots_extracted

