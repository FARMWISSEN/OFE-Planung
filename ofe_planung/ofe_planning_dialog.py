    def initialize_map(self):
        """Initialize the map canvas with an OpenStreetMap background and zoom to a default coordinate."""
        self.mapCanvas.setCanvasColor(Qt.white)
        if self.mapWidget.layout() is None:
            self.layout_map = QVBoxLayout(self.mapWidget)
        else:
            self.layout_map = self.mapWidget.layout()
        self.layout_map.addWidget(self.mapCanvas)
                
        self.mapCanvas.setDestinationCrs(self.target_crs)
        
        self.pan_tool = QgsMapToolPan(self.mapCanvas)
        self.mapCanvas.setMapTool(self.pan_tool)
        
