    def _create_headland_layers(self):
        """Create inner and buffer memory layers from the field boundary."""
        distance = float(self.vorgewendebreite) * -1

        bufferLayer = QgsVectorLayer(
            "Polygon?crs={}".format(self.target_crs.authid()),
            self.tr("Vorgewende"), "memory")
        innerLayer = QgsVectorLayer(
            "Polygon?crs={}".format(self.target_crs.authid()),
            self.tr("Innerfläche"), "memory")

        bufferFeatures = []
        innerFeatures = []

        for feature in self.feld_layer.getFeatures():
            fgeom = QgsGeometry(feature.geometry())
            buffergeom = fgeom.buffer(distance, HEADLAND_BUFFER_SEGMENTS)
            diffgeom = fgeom.difference(buffergeom) if distance < 0 else buffergeom.difference(fgeom)

            bufferFeature = QgsFeature(feature.fields())
            bufferFeature.setGeometry(diffgeom)
            bufferFeatures.append(bufferFeature)

            innerFeature = QgsFeature(feature.fields())
            innerFeature.setGeometry(buffergeom)
            innerFeatures.append(innerFeature)

        bufferLayer.startEditing()
        bufferLayer.dataProvider().addFeatures(bufferFeatures)
        bufferLayer.commitChanges()
        bufferLayer.renderer().setSymbol(create_headland_pattern_symbol())
        bufferLayer.triggerRepaint()
        self.bufferLayer = bufferLayer

        innerLayer.startEditing()
        innerLayer.dataProvider().addFeatures(innerFeatures)
        innerLayer.commitChanges()
        innerLayer.renderer().setSymbol(create_headland_inner_symbol())
        innerLayer.triggerRepaint()
        self.innerLayer = innerLayer

