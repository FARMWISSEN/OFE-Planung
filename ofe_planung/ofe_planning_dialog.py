    def permutate(self, items, count):
        subsets = [[]]
        
        for el in items:
            last = len(subsets) - 1
            for i in range(last + 1):
                arr = subsets[i] + [el]
                if len(arr) == count:
                    subsets.append(arr)
        
        result = [x for x in subsets if len(x) == count]
        return result
    
    def generate_groups(self, variants_count, repetitions, keep_order=False):
        groups = []
        
        if len(groups) < repetitions:
            variants_arr = list(range(variants_count))
            groups.extend(self.permutate(variants_arr, variants_count))
            if not keep_order:
                random.shuffle(groups)
            if len(groups) < repetitions:
                diff = repetitions - len(groups)
                for i in range(diff):
                    shuffled = variants_arr[:]
                    random.shuffle(shuffled)
                    groups.append(shuffled)
            else:
                groups = groups[:repetitions]
        elif len(groups) > repetitions:
            groups = groups[:repetitions]
        
        return groups

