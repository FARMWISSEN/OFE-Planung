    def _do_create_core_plots(self, plots_layer):
        """Generate core plots from plots_layer using current UI settings and add to project."""
        # Need AB line for orientation
        ab_geom = None
        if is_layer_valid(self.ab_line_layer):
            feats = list(self.ab_line_layer.getFeatures())
            if feats:
                ab_geom = feats[0].geometry()
        if not ab_geom or ab_geom.isEmpty():
            QMessageBox.warning(self, self.tr("Warnung"),
                                self.tr("Keine AB-Linie gefunden. Bitte erstellen Sie zuerst Parzellen."))
            return

        prefix = self.txt_prefix_t4.text().strip()
        if not prefix:
            prefix = "core_"

        core_length = self.num_core_length_t4.value()
        core_width = self.num_core_width_t4.value()

        placement_idx = self.cmb_placement_t4.currentIndex()
        placement_map = {0: "center", 1: "bottom", 2: "top"}
        placement = placement_map.get(placement_idx, "center")

        core_features = generate_core_plots(
            plots_layer, ab_geom, core_width, core_length, placement
        )

        if not core_features:
            QMessageBox.warning(self, self.tr("Warnung"),
                                self.tr("Es konnten keine Kernparzellen erstellt werden."))
            return

        # Remove previous core plot layers
        if self.core_plot_layers:
            old_ids = [cl.id() for cl in self.core_plot_layers if is_layer_valid(cl)]
            if old_ids:
                QgsProject.instance().removeMapLayers(old_ids)
            self.core_plot_layers = []

        # Create memory layer for core plots
        crs = plots_layer.crs()
        layer_name = f"{prefix}plots"
        core_layer = QgsVectorLayer(
            f"Polygon?crs={crs.authid()}", layer_name, "memory"
        )
        provider = core_layer.dataProvider()
        provider.addAttributes([
            QgsField("ID", QMetaType.Type.Int, "int"),
            QgsField("LABEL", QMetaType.Type.QString, "string"),
            QgsField("WDH", QMetaType.Type.Int, "int"),
            QgsField("FAKTOR1", QMetaType.Type.QString, "string"),
            QgsField("FAKTOR2", QMetaType.Type.QString, "string"),
            QgsField("VARIANTE", QMetaType.Type.QString, "string"),
        ])
        core_layer.updateFields()

        core_layer.startEditing()
        for feat in core_features:
            new_feat = QgsFeature(core_layer.fields())
            new_feat.setGeometry(feat.geometry())
            attrs = feat.attributes()
            new_feat.setAttributes(attrs[:6] if len(attrs) >= 6 else attrs)
            provider.addFeature(new_feat)
        core_layer.commitChanges()

        # Apply variant renderer
        apply_categorized_variant_renderer(core_layer, list(self.faktorstufen1))

        # Add to project
        layer_group = get_layer_group(self.group_name)
        # Remove any pre-existing layer with the same name to avoid duplicates.
        for _dup in QgsProject.instance().mapLayersByName(layer_name):
            QgsProject.instance().removeMapLayers([_dup.id()])
        QgsProject.instance().addMapLayer(core_layer, False)
        # Insert just above the trial plots layer.
        self._insert_core_layer_in_group(layer_group, core_layer)
        self.core_plot_layers.append(core_layer)

        # Update canvas
        self.mapCanvas.setLayers(self.get_canvas_layers())
        self.mapCanvas.refresh()
        if self.plugin_instance and self.plugin_instance.iface:
            self.plugin_instance.iface.layerTreeView().refreshLayerSymbology(core_layer.id())
            self.plugin_instance.iface.mapCanvas().refresh()

        logger.info("Created %d core plots in layer '%s'", len(core_features), layer_name)

