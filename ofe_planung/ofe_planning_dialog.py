    def _print_t5(self):
        """Render selected layers to a PDF using a QGIS print layout."""
        format_text = self.cmb_print_format_t5.currentText()
        pdf_path = self.txt_print_path_t5.text().strip()
        page_w, page_h = self._PAGE_SIZES_MM.get(format_text, (210.0, 297.0))

        # Layers in draw order: top → bottom, OSM as background
        layers = []
        for combo in [
            self.cmb_guidance_t5, self.cmb_core_t5, self.cmb_plots_t5, self.cmb_af_t5,
            self.cmb_inner_t5, self.cmb_headland_t5, self.cmb_feldgrenze_t5,
        ]:
            layer = combo.currentLayer()
            if layer and layer.isValid():
                layers.append(layer)
        if is_layer_valid(self.osm_layer):
            layers.append(self.osm_layer)

        if not layers:
            QMessageBox.warning(
                self,
                self.tr("Warnung"),
                self.tr("Bitte wählen Sie mindestens einen Layer zum Drucken aus."),
            )
            return

        # --- Compute print extent FIRST so scale bar and map item are consistent ---
        canvas_crs = self.mapCanvas.mapSettings().destinationCrs()
        transform_context = QgsProject.instance().transformContext()

        def _extent_in_canvas_crs(layer):
            """Return layer extent in canvas_crs, or None on any failure."""
            if not is_layer_valid(layer):
                return None
            try:
                ext = layer.extent()
                if ext.isNull() or ext.isEmpty():
                    return None
                src_crs = layer.crs()
                if src_crs.isValid() and src_crs != canvas_crs:
                    ct = QgsCoordinateTransform(src_crs, canvas_crs, transform_context)
                    ext = ct.transformBoundingBox(ext)
                if ext.isNull() or ext.isEmpty():
                    return None
                return ext
            except Exception:
                return None

        # Priority: field combo → plugin feld_layer → canvas extent
        print_extent = _extent_in_canvas_crs(self.cmb_feldgrenze_t5.currentLayer())
        if print_extent is None:
            print_extent = _extent_in_canvas_crs(self.feld_layer)
        if print_extent is None:
            print_extent = self.mapCanvas.extent()

        pad = max(print_extent.width(), print_extent.height()) * 0.05
        print_extent.grow(pad)

        project = QgsProject.instance()
        layout = QgsPrintLayout(project)
        layout.initializeDefaults()

        # Page size
        page = layout.pageCollection().pages()[0]
        page.setPageSize(QgsLayoutSize(page_w, page_h, QgsUnitTypes.LayoutMillimeters))

        # Layout geometry constants (all in mm)
        margin = 10.0
        gap = 3.0
        _CHARS_PER_LINE = 250
        _LINE_HEIGHT_MM = 5.0
        _MAX_LINES = 6
        fragestellung = self.txt_fragestellung.toPlainText().strip()
        # Truncate to max lines and append ellipsis if needed
        max_chars = _CHARS_PER_LINE * _MAX_LINES
        if len(fragestellung) > max_chars:
            fragestellung = fragestellung[:max_chars - 1].rstrip() + "…"
        n_lines = max(1, math.ceil(len(fragestellung) / _CHARS_PER_LINE))
        title_h = n_lines * _LINE_HEIGHT_MM
        right_panel_w = 50.0
        north_size = 20.0
        scale_h = 12.0
        map_left = margin
        map_top = margin + title_h + gap
        map_w = page_w - 2 * margin - gap - right_panel_w
        map_h = page_h - map_top - gap - scale_h - 5.0  # 5 mm bottom margin
        scale_top = map_top + map_h + gap
        right_x = map_left + map_w + gap
        # North arrow sits at the very bottom of the right panel
        north_top = map_top + map_h - north_size
        legend_h = map_h - north_size - gap

        # --- Trial question label ---
        # Width limited to map_w so it doesn't overlap the right panel
        title_label = QgsLayoutItemLabel(layout)
        layout.addLayoutItem(title_label)
        title_label.setText(fragestellung)
        title_label.setFont(QFont("Arial", 9))
        title_label.setHAlign(Qt.AlignLeft)
        title_label.setVAlign(Qt.AlignTop)
        title_label.attemptMove(QgsLayoutPoint(margin, margin, QgsUnitTypes.LayoutMillimeters))
        title_label.attemptResize(QgsLayoutSize(map_w, title_h, QgsUnitTypes.LayoutMillimeters))

        # --- Map item ---
        map_item = QgsLayoutItemMap(layout)
        layout.addLayoutItem(map_item)
        map_item.attemptMove(QgsLayoutPoint(map_left, map_top, QgsUnitTypes.LayoutMillimeters))
        map_item.attemptResize(QgsLayoutSize(map_w, map_h, QgsUnitTypes.LayoutMillimeters))
        map_item.setKeepLayerSet(True)
        map_item.setLayers(layers)
        map_item.setCrs(canvas_crs)
        map_item.zoomToExtent(print_extent)

        # --- North arrow (bottom of right panel, bundled SVG) ---
        north = QgsLayoutItemPicture(layout)
        layout.addLayoutItem(north)
        north_svg = os.path.join(os.path.dirname(__file__), 'icons', 'north_arrow.svg')
        north.setPicturePath(north_svg)
        north.setNorthMode(QgsLayoutItemPicture.GridNorth)
        north.setLinkedMap(map_item)
        north.attemptMove(QgsLayoutPoint(right_x, north_top, QgsUnitTypes.LayoutMillimeters))
        north.attemptResize(QgsLayoutSize(north_size, north_size, QgsUnitTypes.LayoutMillimeters))

        # --- Legend (core_plots excluded) ---
        legend = QgsLayoutItemLegend(layout)
        layout.addLayoutItem(legend)
        legend.setLinkedMap(map_item)
        # Disable auto-update BEFORE modifying the model so the changes stay
        # local to the legend and do NOT alter the project's layer tree.
        legend.setAutoUpdateModel(False)
        legend.updateLegend()
        core_layer = self.cmb_core_t5.currentLayer()
        if core_layer:
            for node in legend.model().rootGroup().findLayers():
                if node.layerId() == core_layer.id():
                    node.parent().removeChildNode(node)
                    break
        # Legend starts at same y as map; zero internal top margin
        from qgis.core import QgsLegendStyle
        legend.rstyle(QgsLegendStyle.Group).setMargin(QgsLegendStyle.Top, 0.0)
        legend.rstyle(QgsLegendStyle.Subgroup).setMargin(QgsLegendStyle.Top, 0.0)
        legend.rstyle(QgsLegendStyle.Title).setMargin(QgsLegendStyle.Top, 0.0)
        legend.attemptMove(QgsLayoutPoint(right_x, map_top, QgsUnitTypes.LayoutMillimeters))
        legend.attemptResize(QgsLayoutSize(right_panel_w, legend_h, QgsUnitTypes.LayoutMillimeters))

        # --- Scale bar ---
        # Compute a nice round segment size from the known print extent so that
        # the bar always shows meaningful distances instead of "0 |".
        scalebar = QgsLayoutItemScaleBar(layout)
        layout.addLayoutItem(scalebar)
        scalebar.setLinkedMap(map_item)
        scalebar.setNumberOfSegments(4)
        scalebar.setNumberOfSegmentsLeft(0)
        scalebar.setUnits(QgsUnitTypes.DistanceMeters)
        scalebar.setUnitLabel("m")
        # Round to the nearest order-of-magnitude step
        raw_seg = print_extent.width() / 4.0
        magnitude = 10 ** math.floor(math.log10(max(raw_seg, 1.0)))
        nice_seg = round(raw_seg / magnitude) * magnitude
        scalebar.setUnitsPerSegment(max(nice_seg, 1.0))
        scalebar.attemptMove(QgsLayoutPoint(margin, scale_top, QgsUnitTypes.LayoutMillimeters))
        scalebar.attemptResize(QgsLayoutSize(map_w * 0.6, scale_h, QgsUnitTypes.LayoutMillimeters))

        try:
            exporter = QgsLayoutExporter(layout)
            settings = QgsLayoutExporter.PdfExportSettings()
            result = exporter.exportToPdf(pdf_path, settings)
            if result == QgsLayoutExporter.Success:
                QMessageBox.information(
                    self,
                    self.tr("Drucken"),
                    self.tr("PDF erfolgreich erstellt:\n{path}").format(path=pdf_path),
                )
            else:
                QMessageBox.critical(
                    self,
                    self.tr("Fehler"),
                    self.tr("PDF konnte nicht erstellt werden (Fehlercode {code}).").format(code=result),
                )
        except Exception as e:
            logger.error("PDF export failed: %s", e, exc_info=True)
            QMessageBox.critical(
                self,
                self.tr("Fehler"),
                self.tr("PDF konnte nicht erstellt werden: {error}").format(error=str(e)),
            )

