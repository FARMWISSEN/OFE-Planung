    def display_blocks_on_map(self, blocks, crs):
        layer_group = get_layer_group(self.group_name)

        # Remove previous block layers before creating new ones
        if self.block_layers:
            old_ids = [bl.id() for bl in self.block_layers if is_layer_valid(bl)]
            if old_ids:
                QgsProject.instance().removeMapLayers(old_ids)
            self.block_layers = []

        block_layers = []

        for block in blocks:
            block_layer = QgsVectorLayer(
                f"Polygon?crs={crs.authid()}",
                block['label'],
                "memory"
            )
            
            provider = block_layer.dataProvider()
            block_layer.startEditing()
            
            for plot in block['plots']:
                feature = QgsFeature()
                feature.setGeometry(plot['geometry'])
                provider.addFeature(feature)
            
            block_layer.commitChanges()
            
            block_layer.renderer().setSymbol(create_block_symbol())
            block_layer.triggerRepaint()
            
            QgsProject.instance().addMapLayer(block_layer, False)
            layer_group.addLayer(block_layer)
            block_layers.append(block_layer)

        # Store reference so cleanup and get_canvas_layers can access them
        self.block_layers = block_layers

        if block_layers:
            self.mapCanvas.setDestinationCrs(crs)

            self.mapCanvas.setLayers(self.get_canvas_layers())
            
            # Zoom to field extent with padding instead of blocks
            if self.feld_layer:
                field_extent = self.feld_layer.extent()
                padding_x = field_extent.width() * FIELD_EXTENT_PADDING_FRACTION
                padding_y = field_extent.height() * FIELD_EXTENT_PADDING_FRACTION
                padded_extent = QgsRectangle(
                    field_extent.xMinimum() - padding_x,
                    field_extent.yMinimum() - padding_y,
                    field_extent.xMaximum() + padding_x,
                    field_extent.yMaximum() + padding_y
                )
                
                self.mapCanvas.setExtent(padded_extent)
                self.mapCanvas.refresh()
                
                if self.plugin_instance and self.plugin_instance.iface:
                    main_canvas = self.plugin_instance.iface.mapCanvas()
                    main_canvas.setDestinationCrs(crs)
                    main_canvas.setExtent(padded_extent)
                    main_canvas.refresh()
                    for layer in block_layers:
                        self.plugin_instance.iface.layerTreeView().refreshLayerSymbology(layer.id())

    def load_saved_params(self):
        """Load previously saved parameters from JSON, populate UI, and restore saved layers."""
        json_path = os.path.join(self.output_dir, "ofe_parameter.json")
        if not os.path.exists(json_path):
            return

        try:
            with open(json_path, "r", encoding="utf-8") as f:
                params = json.load(f)
        except (json.JSONDecodeError, OSError) as e:
            logger.warning("Could not load saved parameters: %s", e)
            return

        # Block all interactive controls that have side-effect signals so that
        # programmatic value-setting during restore doesn't trigger headland
        # recreation, canvas resets, etc.
        _signal_widgets = [
            self.checkbox_vorgewende, self.checkbox_vorgewende_t3,
            self.num_vorgewende, self.num_vorgewende_t3,
            self.int_faktoren, self.int_wiederholungen,
        ]
        for w in _signal_widgets:
            w.blockSignals(True)

        try:
            # --- Tab 1 fields ---
            self.txt_fragestellung.setPlainText(params.get("fragestellung", ""))
            self.txt_faktor1.setText(params.get("faktor1", ""))
            self.txt_faktorstufen1.setText(params.get("faktorstufen1_raw", ""))

            n_faktoren = params.get("n_faktoren", 1)
            self.int_faktoren.setValue(n_faktoren)
            self.on_faktoren_changed(n_faktoren)
            if n_faktoren == 2:
                self.txt_faktor2.setText(params.get("faktor2", ""))
                self.txt_faktorstufen2.setText(params.get("faktorstufen2_raw", ""))

            self.int_wiederholungen.setValue(params.get("wiederholungen", 1))
            self.faktorstufen1 = params.get("faktorstufen", [])

            # Versuchstyp
            vt_name = params.get("versuchstyp", "STREIFEN")
            try:
                self.versuchstyp = Versuchstyp[vt_name]
            except KeyError:
                self.versuchstyp = Versuchstyp.STREIFEN

            # Type-specific spinbox/checkbox fields
            ohne_vorgewende = params.get("ohne_vorgewende", False)
            vorgewendebreite = params.get("vorgewendebreite", 0)
            if self.versuchstyp is Versuchstyp.KLEINPARZELLE:
                self.num_parzellenbreite_x.setValue(params.get("parzellenbreite", 0))
                self.num_parzellenbreite_y.setValue(params.get("parzellenlaenge", 0))
                self.int_anzahl_x.setValue(params.get("anzahl_x", 0))
                self.int_anzahl_y.setValue(params.get("anzahl_y", 0))
                self.num_gap_x.setValue(params.get("gap_x", 0))
                self.num_gap_y.setValue(params.get("gap_y", 0))
                self.num_vorgewende_t3.setValue(vorgewendebreite)
                self.checkbox_vorgewende_t3.setChecked(ohne_vorgewende)
            else:
                self.num_parzellenbreite.setValue(params.get("parzellenbreite", 0))
                self.num_vorgewende.setValue(vorgewendebreite)
                self.checkbox_vorgewende.setChecked(ohne_vorgewende)

            # Set headland instance vars directly (no signal needed)
            self.ohne_vorgewende = ohne_vorgewende
            self.vorgewendebreite = vorgewendebreite

            # --- Tab 4 core plot fields ---
            self.txt_prefix_t4.setText(params.get("core_prefix", "core_"))
            self.num_core_length_t4.setValue(float(params.get("core_length", 0)))
            self.num_core_width_t4.setValue(float(params.get("core_width", 0)))
            self.cmb_placement_t4.setCurrentIndex(int(params.get("core_placement", 0)))
        finally:
            for w in _signal_widgets:
                w.blockSignals(False)

        # --- Restore persisted layers ---
        layer_files = params.get("layer_files", {})
        if not layer_files:
            logger.info("Loaded saved parameters (no layer files) from %s", json_path)
            return

        layer_group = get_layer_group(self.group_name)

        def _load_layer(key, name, provider="ogr"):
            path = layer_files.get(key)
            if not path:
                logger.debug("load_saved_params: no path saved for '%s'", key)
                return None, False
            if not os.path.exists(path):
                logger.warning("load_saved_params: file missing for '%s': %s", key, path)
                return None, False
            # Reuse existing layer to preserve user-customised styling
            existing = next(
                (l for l in QgsProject.instance().mapLayers().values()
                 if l.source().split("|")[0] == path),
                None
            )
            if existing:
                logger.debug("load_saved_params: reused existing '%s' as '%s'", key, name)
                return existing, False
            layer = QgsVectorLayer(path, name, provider)
            if not layer.isValid():
                logger.warning("load_saved_params: layer invalid for '%s': %s", key, path)
                return None, False
            clean_layer_add(layer)
            layer_group.addLayer(layer)
            logger.debug("load_saved_params: loaded '%s' as '%s'", key, name)
            return layer, True

        # Restore combo box selections to original user-selected source layers
        def _restore_combo(layer_id, *combos):
            if not layer_id:
                return
            src = QgsProject.instance().mapLayer(layer_id)
            if src:
                for combo in combos:
                    combo.setLayer(src)

        _restore_combo(
            params.get("selected_feld_id"),
            self.mapLayerComboBox_Feldgrenze,
            self.mapLayerComboBox_Feldgrenze_t3,
        )
        _restore_combo(
            params.get("selected_lenkspur_id"),
            self.mapLayerComboBox_Lenkspur,
            self.mapLayerComboBox_Lenkspur_t3,
        )
        _restore_combo(
            params.get("selected_af_id"),
            self.mapLayerComboBox_AF,
            self.mapLayerComboBox_AF_t3,
        )

        # Field boundary (plugin-reprojected copy)
        feld, feld_new = _load_layer("feld_layer", params.get("feld_layer_name", "Feldgrenze"))
        if feld:
            if feld_new:
                feld.renderer().setSymbol(create_field_symbol())
            self.feld_layer = feld
            self.target_crs = feld.crs()
            self.mapCanvas.setDestinationCrs(self.target_crs)
            self._applied_feld_id = params.get("selected_feld_id", feld.id())

        # Guidance line
        lenkspur, _ = _load_layer("lenkspur_layer", params.get("lenkspur_layer_name", "Lenkspur"))
        if lenkspur:
            self.lenkspur_layer = lenkspur
            self._applied_lenkspur_id = params.get("selected_lenkspur_id", lenkspur.id())

        # Exclusion / obstacle area
        af, af_new = _load_layer("af_layer", params.get("af_layer_name", "Ausschlussfläche"))
        if af:
            if af_new:
                af.renderer().setSymbol(create_obstacle_symbol())
            self.af_layer = af
            self._applied_af_id = params.get("selected_af_id", af.id())

        # Inner area (headland)
        inner, inner_new = _load_layer("inner_layer", params.get("inner_layer_name", "Inner area"))
        if inner:
            if inner_new:
                inner.renderer().setSymbol(create_headland_inner_symbol())
            self.innerLayer = inner

        # Buffer / headland pattern
        buf, buf_new = _load_layer("buffer_layer", params.get("buffer_layer_name", "Headland"))
        if buf:
            if buf_new:
                buf.renderer().setSymbol(create_headland_pattern_symbol())
            self.bufferLayer = buf

        # Restore headland active state directly — no setChecked to avoid signal cascade
        if self.innerLayer and self.bufferLayer:
            self.buffer_is_active = True

        # AB line
        ab, ab_new = _load_layer("ab_line_layer", params.get("ab_line_layer_name", "AB Line"))
        if ab:
            if ab_new:
                ab.renderer().setSymbol(create_ab_line_symbol())
            self.ab_line_layer = ab
            # Restore maptool geometry state so plot creation/randomisation work
            pt_a_coords = params.get("ab_pt_a")
            pt_b_coords = params.get("ab_pt_b")
            if pt_a_coords and pt_b_coords:
                pt_a = QgsPointXY(pt_a_coords[0], pt_a_coords[1])
                pt_b = QgsPointXY(pt_b_coords[0], pt_b_coords[1])
                self.maptool.p0 = pt_a
                self.maptool.pt_A = pt_a
                self.maptool.pt_B = pt_b
                self.maptool.ab_line = QgsGeometry.fromPolylineXY([pt_a, pt_b])
                logger.debug("load_saved_params: restored maptool AB line A=%s B=%s", pt_a_coords, pt_b_coords)

        # Plot layer
        plot, plot_new = _load_layer("plot_layer", params.get("plot_layer_name", "Trial plots"))
        if plot:
            if plot_new:
                apply_categorized_variant_renderer(plot, list(self.faktorstufen1))
            self.plot_layer = plot

        # Core plot layers
        for entry in layer_files.get("core_plot_layers", []):
            path = entry.get("path")
            name = entry.get("name", "core_plots")
            if not path or not os.path.exists(path):
                logger.warning("load_saved_params: core plot file missing: %s", path)
                continue
            # Check if this file is already loaded in the project to avoid duplicates.
            already = next(
                (l for l in QgsProject.instance().mapLayers().values()
                 if l.source().split("|")[0] == path),
                None
            )
            if already:
                self.core_plot_layers.append(already)
                logger.debug("load_saved_params: reused existing core plot layer '%s'", name)
                continue
            cl = QgsVectorLayer(path, name, "ogr")
            if cl.isValid():
                apply_categorized_variant_renderer(cl, list(self.faktorstufen1))
                QgsProject.instance().addMapLayer(cl, False)
                self._insert_core_layer_in_group(layer_group, cl)
                self.core_plot_layers.append(cl)
                logger.debug("load_saved_params: restored core plot layer '%s'", name)
            else:
                logger.warning("load_saved_params: core plot layer invalid: %s", path)

        # Enable the correct planning tab
        if self.versuchstyp is Versuchstyp.KLEINPARZELLE:
            self.tabWidget.setTabEnabled(2, True)
            self.tabWidget.setCurrentIndex(2)
        else:
            self.tabWidget.setTabEnabled(1, True)
            self.tabWidget.setCurrentIndex(1)

        # Enable Core Plots tab if plots exist
        if self.plot_layer and self.ab_line_layer:
            self.tabWidget.setTabEnabled(3, True)
            self._preselect_t4_layers()
        elif self.core_plot_layers:
            self.tabWidget.setTabEnabled(3, True)
            self._preselect_t4_layers()

        # Update canvas directly — showEvent acts as secondary sync for edge cases
        canvas_layers = self.get_canvas_layers()
        if canvas_layers:
            self.mapCanvas.setLayers(canvas_layers)

        if self.feld_layer:
            field_extent = self.feld_layer.extent()
            padding_x = field_extent.width() * FIELD_EXTENT_PADDING_FRACTION
            padding_y = field_extent.height() * FIELD_EXTENT_PADDING_FRACTION
            self.mapCanvas.setExtent(QgsRectangle(
                field_extent.xMinimum() - padding_x,
                field_extent.yMinimum() - padding_y,
                field_extent.xMaximum() + padding_x,
                field_extent.yMaximum() + padding_y,
            ))

        self.mapCanvas.refresh()

        logger.info("Restored saved session from %s - %d canvas layers: %s",
                    json_path, len(canvas_layers), [l.name() for l in canvas_layers])

        if self.plugin_instance and self.plugin_instance.iface:
            self.plugin_instance.iface.mapCanvas().refresh()

    def read_parameters(self):
        self.parzellenbreite = self.num_parzellenbreite.value()
        self.parzellenlaenge = 0.0
        self.gap_x = 0
        self.gap_y = 0

        self.wdh = self.int_wiederholungen.value()        
        self.vorgewendebreite = self.num_vorgewende.value()
        self.ohne_vorgewende = self.checkbox_vorgewende.isChecked()
        self.faktor1 = self.txt_faktor1.text().strip()
        
        faktorstufen1 = self.txt_faktorstufen1.text().strip()
        stufen1 = [s.strip() for s in faktorstufen1.split(',')]
        
        n_faktoren = self.int_faktoren.value()
        
        if n_faktoren == 2:
            self.faktor2 = self.txt_faktor2.text().strip()
            faktorstufen2 = self.txt_faktorstufen2.text().strip()
            stufen2 = [s.strip() for s in faktorstufen2.split(',') if s.strip()]
            
            if len(stufen1) == 1:
                QtWidgets.QMessageBox.warning(
                    self, 
                    self.tr("Warnung: Nur 1 Faktorstufe"),
                    self.tr("Faktor 1 hat nur eine Stufe: '{level}'\n\n"
                            "Für mehrere Faktorstufen bitte kommagetrennt eingeben, z.B.: a, b, c").format(level=stufen1[0])
                )
            if len(stufen2) == 1:
                QtWidgets.QMessageBox.warning(
                    self, 
                    self.tr("Warnung: Nur 1 Faktorstufe"),
                    self.tr("Faktor 2 hat nur eine Stufe: '{level}'\n\n"
                            "Für mehrere Faktorstufen bitte kommagetrennt eingeben, z.B.: x, y, z").format(level=stufen2[0])
                )
            
            self.faktorstufen1 = []
            for s1 in stufen1:
                for s2 in stufen2:
                    self.faktorstufen1.append(f"{s1}_{s2}")
        else:
            self.faktorstufen1 = stufen1
            if len(stufen1) == 1:
                QtWidgets.QMessageBox.warning(
                    self, 
                    self.tr("Warnung: Nur 1 Variante"),
                    self.tr("Es wurde nur eine Variante gefunden: '{level}'\n\n"
                            "Für mehrere Varianten bitte kommagetrennt eingeben, z.B.: V1, V2, V3").format(level=stufen1[0])
                )
        
        self.anzahl_x = self.wdh * len(self.faktorstufen1)
        self.anzahl_y = 1  # strip trials only

        # Special handling for small-plot experiments
        if self.versuchstyp is Versuchstyp.KLEINPARZELLE:
            self.parzellenbreite = self.num_parzellenbreite_x.value()
            self.parzellenlaenge = self.num_parzellenbreite_y.value()
            self.anzahl_x=self.int_anzahl_x.value()
            self.anzahl_y=self.int_anzahl_y.value()
            self.vorgewendebreite = self.num_vorgewende_t3.value()
            self.ohne_vorgewende = self.checkbox_vorgewende_t3.isChecked()
            self.gap_x = self.num_gap_x.value()
            self.gap_y = self.num_gap_y.value()
            logger.debug("read_parameters - anzahl_y: %d", self.anzahl_y)
        

