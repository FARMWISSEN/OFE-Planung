    def closeEvent(self, event):
        """Intercept all close attempts (X button, ESC, cancel) and confirm with the user."""
        if not self.isVisible():
            event.accept()
            return

        reply = QMessageBox.question(
            self,
            self.tr("Planung beenden"),
            self.tr("Alle nicht gespeicherten Planungsdaten gehen verloren. Möchten Sie wirklich beenden?"),
            QMessageBox.Yes | QMessageBox.No,
            QMessageBox.No,
        )
        if reply != QMessageBox.Yes:
            event.ignore()
            return
        self._cleanup_layers()
        self._reset_state()
        event.accept()

    def _cleanup_layers(self):
        """Remove in-memory plugin layers; leave file-based (saved) layers in the project."""
        layers_to_remove = []
        tracked = [
            self.plot_layer, self.ab_line_layer, self.lenkspur_layer,
            self.af_layer, self.innerLayer, self.bufferLayer, self.feld_layer,
        ] + list(self.block_layers) + list(self.core_plot_layers)
        for layer in tracked:
            if is_layer_valid(layer) and layer.providerType() == "memory":
                layers_to_remove.append(layer.id())
        if layers_to_remove:
            QgsProject.instance().removeMapLayers(layers_to_remove)
        self.block_layers = []
        self.core_plot_layers = []

        # Only remove the group if it is now empty
        root = QgsProject.instance().layerTreeRoot()
        layer_group = root.findGroup(self.group_name)
        if layer_group and not layer_group.findLayers():
            root.removeChildNode(layer_group)

        if hasattr(self, 'maptool'):
            try:
                self.maptool.hide()
                self.mapCanvas.unsetMapTool(self.maptool)
            except Exception:
                pass

        self.mapCanvas.setLayers([])
        self.mapCanvas.refresh()

        if self.plugin_instance and self.plugin_instance.iface:
            self.plugin_instance.iface.mapCanvas().refresh()

