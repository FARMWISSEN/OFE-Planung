    def _insert_core_layer_in_group(self, layer_group, layer):
        """Insert a core plot layer just above the trial plots layer in the group."""
        new_node = QgsLayerTreeLayer(layer)
        if is_layer_valid(self.plot_layer):
            for i, child in enumerate(layer_group.children()):
                if isinstance(child, QgsLayerTreeLayer) and child.layer() == self.plot_layer:
                    layer_group.insertChildNode(i, new_node)
                    return
        # Fallback: append at bottom of group
        layer_group.insertChildNode(-1, new_node)

