    def _layer_is_persisted(self, layer):
        """Return True if layer is already saved as a file inside output_dir."""
        if layer.dataProvider().name() != "ogr":
            return False
        src = os.path.normcase(os.path.normpath(layer.source().split("|")[0]))
        out = os.path.normcase(os.path.normpath(self.output_dir))
        return src.startswith(out + os.sep) or src.startswith(out + "/")

    def on_speicher_button_clicked(self):
        """Save all parameters to JSON and persist in-memory layers as shapefiles."""
        if not is_layer_valid(self.plot_layer):
            QMessageBox.warning(self, self.tr("Warnung"), self.tr("Keine Versuchsparzellen zum Speichern."))
            return False

        # --- 1. Collect parameters ---
        params = {
            "versuchstyp": self.versuchstyp.name,
            "fragestellung": self.txt_fragestellung.toPlainText().strip(),
            "faktor1": self.faktor1,
            "faktor2": self.faktor2,
            "faktorstufen1_raw": self.txt_faktorstufen1.text().strip(),
            "faktorstufen": self.faktorstufen1,
            "n_faktoren": self.int_faktoren.value(),
            "wiederholungen": self.wdh,
            "parzellenbreite": self.parzellenbreite,
            "parzellenlaenge": self.parzellenlaenge,
            "gap_x": self.gap_x,
            "gap_y": self.gap_y,
            "vorgewendebreite": self.vorgewendebreite,
            "ohne_vorgewende": self.ohne_vorgewende,
            "anzahl_x": self.anzahl_x,
            "anzahl_y": self.anzahl_y,
            "core_prefix": self.txt_prefix_t4.text(),
            "core_length": self.num_core_length_t4.value(),
            "core_width": self.num_core_width_t4.value(),
            "core_placement": self.cmb_placement_t4.currentIndex(),
        }

        if self.int_faktoren.value() == 2:
            params["faktorstufen2_raw"] = self.txt_faktorstufen2.text().strip()

        # Add layer file references
        layer_files = {}

        # --- 2. Persist in-memory layers ---
        layer_group = get_layer_group(self.group_name)

        # Plot layer
        if self._layer_is_persisted(self.plot_layer):
            layer_files["plot_layer"] = self.plot_layer.source().split("|")[0]
            params["plot_layer_name"] = self.plot_layer.name()
        else:
            plot_file = os.path.join(self.output_dir, self.plot_layer.name() + ".shp")
            success, msg = export_shapefile(self.plot_layer, plot_file)
            if success:
                saved_plot = QgsVectorLayer(plot_file, self.plot_layer.name(), "ogr")
                if saved_plot.isValid():
                    apply_categorized_variant_renderer(saved_plot, list(self.faktorstufen1))
                    remove_layer(self.plot_layer)
                    self.plot_layer = saved_plot
                    clean_layer_add(self.plot_layer)
                    layer_group.addLayer(self.plot_layer)
                layer_files["plot_layer"] = plot_file
                params["plot_layer_name"] = self.plot_layer.name()

        # AB line layer
        if is_layer_valid(self.ab_line_layer):
            if self._layer_is_persisted(self.ab_line_layer):
                layer_files["ab_line_layer"] = self.ab_line_layer.source().split("|")[0]
                params["ab_line_layer_name"] = self.ab_line_layer.name()
            else:
                ab_file = os.path.join(self.output_dir, self.ab_line_layer.name() + ".shp")
                success, msg = export_shapefile(self.ab_line_layer, ab_file)
                if success:
                    saved_ab = QgsVectorLayer(ab_file, self.ab_line_layer.name(), "ogr")
                    if saved_ab.isValid():
                        saved_ab.renderer().setSymbol(create_ab_line_symbol())
                        remove_layer(self.ab_line_layer)
                        self.ab_line_layer = saved_ab
                        clean_layer_add(self.ab_line_layer)
                        layer_group.addLayer(self.ab_line_layer)
                    layer_files["ab_line_layer"] = ab_file
                    params["ab_line_layer_name"] = self.ab_line_layer.name()
        # Save AB line endpoints so maptool state can be restored
        if is_layer_valid(self.ab_line_layer):
            feats = list(self.ab_line_layer.getFeatures())
            if feats:
                geom = feats[0].geometry()
                if geom.isMultipart():
                    parts = geom.asMultiPolyline()
                    line = parts[0] if parts else []
                else:
                    line = geom.asPolyline()
                if len(line) >= 2:
                    params["ab_pt_a"] = [line[0].x(), line[0].y()]
                    params["ab_pt_b"] = [line[-1].x(), line[-1].y()]

        # Inner area layer (headland)
        if is_layer_valid(self.innerLayer):
            if self._layer_is_persisted(self.innerLayer):
                layer_files["inner_layer"] = self.innerLayer.source().split("|")[0]
                params["inner_layer_name"] = self.innerLayer.name()
            else:
                inner_file = os.path.join(self.output_dir, self.innerLayer.name() + ".shp")
                success, msg = export_shapefile(self.innerLayer, inner_file)
                if success:
                    saved_inner = QgsVectorLayer(inner_file, self.innerLayer.name(), "ogr")
                    if saved_inner.isValid():
                        saved_inner.renderer().setSymbol(create_headland_inner_symbol())
                        remove_layer(self.innerLayer)
                        self.innerLayer = saved_inner
                        clean_layer_add(self.innerLayer)
                        layer_group.addLayer(self.innerLayer)
                    layer_files["inner_layer"] = inner_file
                    params["inner_layer_name"] = self.innerLayer.name()

        # Buffer layer (headland pattern)
        if is_layer_valid(self.bufferLayer):
            if self._layer_is_persisted(self.bufferLayer):
                layer_files["buffer_layer"] = self.bufferLayer.source().split("|")[0]
                params["buffer_layer_name"] = self.bufferLayer.name()
            else:
                buffer_file = os.path.join(self.output_dir, self.bufferLayer.name() + ".shp")
                success, msg = export_shapefile(self.bufferLayer, buffer_file)
                if success:
                    saved_buffer = QgsVectorLayer(buffer_file, self.bufferLayer.name(), "ogr")
                    if saved_buffer.isValid():
                        saved_buffer.renderer().setSymbol(create_headland_pattern_symbol())
                        remove_layer(self.bufferLayer)
                        self.bufferLayer = saved_buffer
                        clean_layer_add(self.bufferLayer)
                        layer_group.addLayer(self.bufferLayer)
                    layer_files["buffer_layer"] = buffer_file
                    params["buffer_layer_name"] = self.bufferLayer.name()

        # Field and guidance layers are already file-based
        if is_layer_valid(self.feld_layer):
            layer_files["feld_layer"] = self.feld_layer.source()
            params["feld_layer_name"] = self.feld_layer.name()
        if is_layer_valid(self.lenkspur_layer):
            layer_files["lenkspur_layer"] = self.lenkspur_layer.source()
            params["lenkspur_layer_name"] = self.lenkspur_layer.name()
        if is_layer_valid(self.af_layer):
            layer_files["af_layer"] = self.af_layer.source()
            params["af_layer_name"] = self.af_layer.name()

        # Persist any existing core plot layers first so their paths are available
        if self.core_plot_layers:
            self._persist_core_plot_layers()

        # Core plot layers (list)
        core_entries = []
        for cl in self.core_plot_layers:
            if is_layer_valid(cl) and self._layer_is_persisted(cl):
                core_entries.append({"path": cl.source().split("|")[0], "name": cl.name()})
        if core_entries:
            layer_files["core_plot_layers"] = core_entries

        # Save original user-selected source layer IDs for combo box restore
        if self._applied_feld_id:
            params["selected_feld_id"] = self._applied_feld_id
        if self._applied_lenkspur_id:
            params["selected_lenkspur_id"] = self._applied_lenkspur_id
        if self._applied_af_id:
            params["selected_af_id"] = self._applied_af_id

        params["layer_files"] = layer_files

        # --- 3. Save JSON ---
        json_path = os.path.join(self.output_dir, "ofe_parameter.json")
        with open(json_path, "w", encoding="utf-8") as f:
            json.dump(params, f, ensure_ascii=False, indent=2)

        # --- 4. Refresh ---
        self.mapCanvas.setLayers(self.get_canvas_layers())
        self.mapCanvas.refresh()
        iface = self.plugin_instance.iface
        for lyr in [self.plot_layer, self.ab_line_layer, self.innerLayer, self.bufferLayer]:
            if is_layer_valid(lyr):
                lyr.triggerRepaint()
                iface.layerTreeView().refreshLayerSymbology(lyr.id())
        iface.mapCanvas().refresh()

        logger.info("Saved experiment to %s", self.output_dir)

        # Enable Core Plots tab after saving
        self.tabWidget.setTabEnabled(3, True)
        self._preselect_t4_layers()

        return True

