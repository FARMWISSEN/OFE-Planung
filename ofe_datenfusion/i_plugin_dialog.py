    def show_variogram_analysis(self):
        """Show variogram analysis dialog with current parameters."""
        # Create progress dialog immediately
        progress = QProgressDialog(
            "Variogramm-Analyse läuft...",
            "Abbrechen",
            0,
            0,
            self
        )
        progress.setWindowTitle("OFE-Datenfusion")
        progress.setWindowModality(Qt.WindowModal)
        progress.setMinimumDuration(0)  # Show immediately
        progress.setAutoClose(True)
        progress.setAutoReset(True)
        progress.setCancelButton(None)  # No cancel button for variogram analysis
        
        try:
            # Show progress dialog immediately
            progress.show()
            QCoreApplication.processEvents()  # Force UI update
            
            # Get current parameters
            params = self.get_kriging_parameters()
            if not params:
                progress.close()
                return
                
            # Get input data
            layer = self.mMapLayerComboBox.currentLayer()
            field = self.mFieldComboBox.currentField()
            boundary = self.mMapLayerComboBox_boundary.currentLayer()
            
            # Prepare data
            # check_duplicates=True hier, da Variogram-Analyse der erste Schritt ist
            x, y, z = self.plugin.prepare_data(layer, field, boundary, check_duplicates=True)
            if x is None or len(x) == 0:
                progress.close()
                QgsMessageLog.logMessage(
                    "No valid data points found for variogram analysis",
                    "OFE-Datenfusion",
                    Qgis.Warning
                )
                return
                
            # Analyze variogram
            results = self.plugin.analyze_variogram(x, y, z, params)
            if not results:
                progress.close()
                return
                
            # Store variogram_info for later use in get_parameters()
            self.variogram_info_raster = results
                
            # Close progress dialog
            progress.close()
            
            # Store results for model comparison
            from datetime import datetime
            model_name = params.get('variogram_model', 'unknown')
            self.variogram_results_raster.append({
                'model': model_name,
                'parameters': results['parameters'],
                'metrics': results['metrics'],
                'nlags': params.get('nlags', 6),
                'timestamp': datetime.now().strftime("%Y-%m-%d %H:%M:%S")
            })
            
            # Enable model comparison button
            if hasattr(self, 'VergleichButton_rasterVariogram'):
                self.VergleichButton_rasterVariogram.setEnabled(True)
                
            # Update dialog with optimized parameters
            self.update_variogram_parameters({
                'nugget': results['parameters']['nugget'],
                'range': results['parameters']['range'],
                'sill': results['parameters']['sill'],
                'metrics': results['metrics']
            }, point_tab=False)
                
            # Show dialog with context information
            dialog = VariogramDialog(
                self,
                layer_name=layer.name() if layer else None,
                field_name=field,
                method="ordinary_kriging",
                is_point_tab=False
            )
            dialog.display_results(results['plot_path'], results['metrics'])
            dialog.exec_()
            
        except InterpolationCalculationError as e:
            progress.close()
            QMessageBox.critical(self, "Variogramm-Analyse fehlgeschlagen", str(e))
        except DataValidationError as e:
            progress.close()
            QMessageBox.warning(self, "Datenvalidierung", str(e))
        except InterpolationError as e:
            progress.close()
            QMessageBox.critical(self, "Fehler", str(e))
        except Exception as e:
            progress.close()
            QgsMessageLog.logMessage(
                f"Unerwarteter Fehler bei Variogramm-Analyse: {str(e)}",
                "OFE-Datenfusion",
                Qgis.Critical
            )
            QMessageBox.critical(self, "Unerwarteter Fehler", f"Ein unerwarteter Fehler ist aufgetreten:\n\n{str(e)}")

