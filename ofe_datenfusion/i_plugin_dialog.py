    def validate_inputs(self):
        """Validate user inputs."""
        # Check input layer
        input_layer = self.mMapLayerComboBox.currentLayer()
        if not input_layer:
            QMessageBox.warning(
                self,
                "Warnung",
                "Bitte wählen Sie einen Eingabelayer aus."
            )
            return False
            
        # Check if input layer has features
        if input_layer.featureCount() == 0:
            QMessageBox.warning(
                self,
                "Warnung",
                "Der ausgewählte Eingabelayer enthält keine Features."
            )
            return False
            
        # Check field selection
        field = self.mFieldComboBox.currentField()
        if not field:
            QMessageBox.warning(
                self,
                "Warnung",
                "Bitte wählen Sie ein Feld für die Interpolation aus."
            )
            return False
            
        # Check if field exists in layer
        if field not in [field.name() for field in input_layer.fields()]:
            QMessageBox.warning(
                self,
                "Warnung",
                "Das ausgewählte Feld existiert nicht im Eingabelayer."
            )
            return False

        # Check for zero values and NULLs in the selected field
        has_zero = False
        has_null = False
        from qgis.PyQt.QtCore import QVariant
        for feature in input_layer.getFeatures():
            value = feature[field]
            # Prüfe auf echte Nullwerte und auf QVariant/None
            if value is None or (isinstance(value, QVariant) and value.isNull()):
                has_null = True
                break
            try:
                if float(value) == 0:
                    has_zero = True
                    break
            except (ValueError, TypeError, AttributeError):
                # Falls ein Wert nicht konvertierbar ist, als ungültig behandeln
                has_null = True
                break
        if has_null:
            QMessageBox.warning(
                self,
                "Warnung",
                "Daten enthalten leere oder ungültige Werte (NULLs). Die Interpolation kann dies nicht verarbeiten. Bitte filtern."
            )
            return False
        if has_zero:
            QMessageBox.warning(
                self,
                "Warnung",
                "Daten enthalten 0 Werte. Die Interpolation kann dies nicht verarbeiten. Bitte filtern."
            )
            return False

        # Check cell size
        if self.doubleSpinBox_cellsize.value() <= 0:
            QMessageBox.warning(
                self,
                "Warnung",
                "Die Zellgröße muss größer als 0 sein."
            )
            return False
            
        # Check boundary layer if selected
        boundary_layer = self.mMapLayerComboBox_boundary.currentLayer()
        if boundary_layer:
            if boundary_layer.featureCount() == 0:
                QMessageBox.warning(
                    self,
                    "Warnung",
                    "Der ausgewählte Grenzlayer enthält keine Features."
                )
                return False
                
            # Check if any boundary polygons intersect with the input points
            has_intersection = False
            for point_feature in input_layer.getFeatures():
                for boundary_feature in boundary_layer.getFeatures():
                    if point_feature.geometry().intersects(boundary_feature.geometry()):
                        has_intersection = True
                        break
                if has_intersection:
                    break
                    
            if not has_intersection:
                reply = QMessageBox.question(
                    self,
                    "Warnung",
                    "Keine Punkte liegen innerhalb der ausgewählten Grenze. "
                    "Möchten Sie trotzdem fortfahren?",
                    QMessageBox.Yes | QMessageBox.No,
                    QMessageBox.No
                )
                if reply == QMessageBox.No:
                    return False
            
        return True
