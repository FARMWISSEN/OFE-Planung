    def validate_point_interpolation_inputs(self):
        """Validiert die Eingaben für die Punkt-Interpolation."""
        # Check target layer
        if not self.mMapLayerComboBox_target_layer.currentLayer():
            QMessageBox.warning(self, "Warnung", "Bitte wählen Sie einen Ziel-Layer aus.")
            return False
        
        # Check covariate layer
        if not self.mMapLayerComboBox_covariate_point.currentLayer():
            QMessageBox.warning(self, "Warnung", "Bitte wählen Sie einen Kovariaten-Layer aus.")
            return False
            
        # Check covariate field (not the raster field!)
        if not self.mFieldComboBox_covariate.currentField():
            QMessageBox.warning(self, "Warnung", "Bitte wählen Sie ein Kovariaten-Feld aus.")
            return False

        # Check for zero and NULL values in the COVARIATE field (not target!)
        has_zero = False
        has_null = False
        from qgis.PyQt.QtCore import QVariant
        
        covariate_layer = self.mMapLayerComboBox_covariate_point.currentLayer()
        covariate_field = self.mFieldComboBox_covariate.currentField()
        
        for feature in covariate_layer.getFeatures():
            value = feature[covariate_field]
            # Check for NULL values
            if value is None or (isinstance(value, QVariant) and value.isNull()):
                has_null = True
                break
            try:
                if float(value) == 0:
                    has_zero = True
                    break
            except (ValueError, TypeError, AttributeError):
                # Value not convertible - treat as invalid
                has_null = True
                break
        
        if has_null:
            QMessageBox.warning(
                self,
                "Warnung",
                "Kovariaten-Daten enthalten leere oder ungültige Werte (NULLs). Die Interpolation kann dies nicht verarbeiten. Bitte filtern."
            )
            return False
            
        if has_zero:
            QMessageBox.warning(
                self,
                "Warnung",
                "Kovariaten-Daten enthalten 0 Werte. Die Interpolation kann dies nicht verarbeiten. Bitte filtern."
            )
            return False
            
        return True

    def interpolate_points(self):
        """Handle point interpolation button click."""
        try:
            # Validate inputs
            if not self.validate_point_interpolation_inputs():
                return
                
            # Get parameters
            params = self.get_point_interpolation_parameters()
            
            # Create progress dialog
            progress = QProgressDialog(
                "Punkt-Interpolation läuft...",
                "Abbrechen",
                0,
                0,
                self
            )
            progress.setWindowTitle("OFE-Datenfusion")
            progress.setWindowModality(Qt.WindowModal)
            progress.setMinimumDuration(0)
            progress.setAutoClose(True)
            progress.setAutoReset(True)
            progress.setMinimum(0)
            progress.setMaximum(0)  # Unbestimmter Fortschritt
            
            # Run interpolation
            if self.plugin:
                try:
                    # Show progress dialog
                    progress.show()
                    
                    # Process events to keep UI responsive
                    QCoreApplication.processEvents()
                    
                    # Run interpolation
                    self.plugin.run_point_interpolation(params)
                    
                    # Close progress dialog
                    progress.close()
                    
                    # Nur bei erfolgreicher Interpolation speichern!
                    self.save_settings()
                    
                    # Erstelle passende Success-Nachricht mit Layer-Kopie Info
                    copy_msg = ""
                    if params.get('copied_layer_name'):
                        copy_msg = f"\n\nEin neuer Layer wurde erstellt: '{params['copied_layer_name']}'\n"
                        copy_msg += "Der Original-Layer bleibt unverändert."
                    
                    QMessageBox.information(
                        self,
                        "Erfolg",
                        f"Punkt-Interpolation erfolgreich abgeschlossen.{copy_msg}"
                    )
                    
                except InterpolationError as e:
                    progress.close()
                    raise
                except Exception as e:
                    progress.close()
                    raise
                    
                finally:
                    progress.close()
            
        except DataValidationError as e:
            QMessageBox.warning(self, "Datenvalidierung", str(e))
        except GeometryError as e:
            QMessageBox.warning(self, "Geometrie-Problem", str(e))
        except InterpolationCalculationError as e:
            QMessageBox.critical(self, "Interpolation fehlgeschlagen", str(e))
        except InterpolationError as e:
            QMessageBox.critical(self, "Fehler", str(e))
        except Exception as e:
            QMessageBox.critical(
                self,
                "Unerwarteter Fehler",
                f"Ein unerwarteter Fehler ist aufgetreten:\n\n{str(e)}"
            )

